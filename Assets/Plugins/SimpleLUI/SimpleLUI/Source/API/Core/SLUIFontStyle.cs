namespace SimpleLUI.API.Core
{
    /// <summary>
    ///     Font Style applied to UI Text.
    /// </summary>
    public enum SLUIFontStyle
    {
        /// <summary>
        /// 	No special style is applied.
        /// </summary>
        Normal,

        /// <summary>
        ///     Bold style applied to your texts.
        /// </summary>
        Bold,

        /// <summary>
        ///     Italic style applied to your texts.
        /// </summary>
        Italic,

        /// <summary>
        ///     Bold and Italic styles applied to your texts.
        /// </summary>
        BoldAndItalic
    }
}
